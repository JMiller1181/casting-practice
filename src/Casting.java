public class Casting {
    public static void main (String[] args) {
        double number = 112.35;
        int num = (int)number;
        System.out.println("Double: " + number);
        System.out.println("Int: " + num);
        String sevenSeven = "49";
        int sevenSquare = Integer.parseInt(sevenSeven);
        System.out.println("The string value is: " + sevenSeven);
        System.out.println("The integer value is: " + sevenSquare);

    }
}
